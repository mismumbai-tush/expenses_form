-- 2. CREATE ADMINS TABLE
-- Stores administrative credentials for Dashboard log in
CREATE TABLE IF NOT EXISTS admins (
    email TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    password TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- 3. CREATE EXPENSE CLAIMS TABLE
-- Stores itemized expense claims submitted by salespeople
CREATE TABLE IF NOT EXISTS expense_claims (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    submission_id TEXT NOT NULL,
    timestamp TEXT,
    branch_name TEXT,
    salesperson_name TEXT,
    salesperson_email TEXT,
    expense_category TEXT,
    item_date TEXT,
    from_location TEXT,
    to_location TEXT,
    amount TEXT,
    attachment_link TEXT,
    item_remark TEXT,
    grand_total TEXT,
    admin_remark TEXT,
    mail_sent TEXT DEFAULT 'No',
    approved TEXT DEFAULT 'No',
    approved_timestamp TEXT,
    payment_process TEXT DEFAULT 'No',
    processed_by TEXT,
    status TEXT DEFAULT 'Pending',
    payment_release TEXT DEFAULT 'No',
    released_by TEXT,
    branch_head_email TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

