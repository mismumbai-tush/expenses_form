-- 5. ROW LEVEL SECURITY (RLS) POLICIES
-- Service role key automatically bypasses RLS.
-- This policy permits public/authenticated reads and writes for simplicity.
ALTER TABLE admins ENABLE ROW LEVEL SECURITY;
ALTER TABLE expense_claims ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow public read and write on admins" 
ON admins FOR ALL 
USING (true) 
WITH CHECK (true);

CREATE POLICY "Allow public read and write on expense_claims" 
ON expense_claims FOR ALL 
USING (true) 
WITH CHECK (true);

